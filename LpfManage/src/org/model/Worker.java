package org.model;

public class Worker {
	private int id;
	private String name;
	private int age;
	private String sex;
	private String tel;
	private int worktimes;
	private int free;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	public int getWorktimes() {
		return worktimes;
	}
	public void setWorktimes(int worktimes) {
		this.worktimes = worktimes;
	}
	public int getFree() {
		return free;
	}
	public void setFree(int free) {
		this.free = free;
	}
	

}
