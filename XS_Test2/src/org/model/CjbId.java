package org.model;
public class CjbId implements java.io.Serializable {
	private String xh;
	private String kch;
	public CjbId() {
	}
	public CjbId(String xh, String kch) {
		this.xh = xh;
		this.kch = kch;
	}
	public String getXh() {
		return this.xh;
	}

	public void setXh(String xh) {
		this.xh = xh;
	}

	public String getKch() {
		return this.kch;
	}

	public void setKch(String kch) {
		this.kch = kch;
	}
}
